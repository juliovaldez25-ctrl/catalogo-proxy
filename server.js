const STATIC_PATHS = [
  /^\/assets\//,
  /^\/favicon\.ico$/,
  /^\/robots\.txt$/,
  /^\/sitemap\.xml$/,
  /^\/site\.webmanifest$/,
  /^\/~flock\.js$/,
  /^\/~api\//,
];
const isStatic = (path) => STATIC_PATHS.some((rx) => rx.test(path));

/* ======================================================
   üß≠ MIDDLEWARE PRINCIPAL (PROXY)
====================================================== */
app.use(async (req, res, next) => {
  const originalHost = req.headers.host?.trim().toLowerCase() || "";
  const cleanHost = originalHost.replace(/^www\./, "");
  const path = req.path;

  console.log(`üåê ${cleanHost} ‚Üí ${path}`);

  if (!cleanHost || cleanHost.includes("railway.app")) {
    return res.status(200).send("‚úÖ Proxy ativo e aguardando conex√µes Cloudflare");
  }

  const domainData = await getDomainData(cleanHost);

  if (!domainData) {
    return res.status(404).send(`
      <html><body style="font-family:sans-serif;text-align:center;margin-top:40px">
      <h2>‚ö†Ô∏è Dom√≠nio n√£o configurado</h2>
      <p>${cleanHost} ainda n√£o foi ativado no Cat√°logo Virtual.</p>
      </body></html>
    `);
  }

  // üîß Define destino (origem Lovable)
  const target = isStatic(path)
    ? CONFIG.ORIGIN
    : `${CONFIG.ORIGIN}/s/${domainData.slug}`;

  console.log(`‚û°Ô∏è Proxy: ${cleanHost}${path} ‚Üí ${target}`);

  return createProxyMiddleware({
    target,
    changeOrigin: true,
    secure: true,
    xfwd: true,
    headers: {
      "Access-Control-Allow-Origin": "*",
      "Access-Control-Allow-Methods": "GET, POST, OPTIONS",
      "Access-Control-Allow-Headers": "*",
      "X-Forwarded-Host": originalHost,
      "X-Forwarded-Proto": "https",
      "User-Agent": req.headers["user-agent"] || "CatalogoProxy",
    },

    onProxyRes(proxyRes, req, res) {
      const enc = proxyRes.headers["content-encoding"];
      const chunks = [];

      proxyRes.on("data", (chunk) => chunks.push(chunk));
      proxyRes.on("end", () => {
        let buffer = Buffer.concat(chunks);
        const contentType = proxyRes.headers["content-type"] || "";

        delete proxyRes.headers["content-encoding"];
        delete proxyRes.headers["content-length"];

        if (enc === "gzip") buffer = zlib.gunzipSync(buffer);
        else if (enc === "br") buffer = zlib.brotliDecompressSync(buffer);

        if (contentType.includes("text/html")) {
          let html = buffer.toString("utf8");

          // ‚úÖ Injeta script de redirecionamento autom√°tico
          html = html.replace(
            "</head>",
            `<script>
              (function() {
                const slug = "${domainData.slug}";
                const pathname = window.location.pathname;
                if (!pathname.startsWith('/s/' + slug)) {
                  if (!pathname.startsWith('/assets') && !pathname.startsWith('/~') && pathname !== '/favicon.ico') {
                    const novaUrl = '/s/' + slug + pathname;
                    console.log('üîÅ Redirecionando automaticamente para:', novaUrl);
                    window.location.replace(novaUrl);
                  }
                }
              })();
            </script>\n</head>`
          );

          res.writeHead(proxyRes.statusCode, {
            ...proxyRes.headers,
            "Access-Control-Allow-Origin": "*",
          });
          res.end(html);
        } else {
          res.writeHead(proxyRes.statusCode, {
            ...proxyRes.headers,
            "Access-Control-Allow-Origin": "*",
          });
          res.end(buffer);
        }
      });
    },

    onError(err, req, res) {
      console.error("‚ùå ProxyError", err.message);
      res
        .status(502)
        .send(`<h2>Erro 502</h2><p>${err.message}</p>`);
    },
  })(req, res, next);
});

